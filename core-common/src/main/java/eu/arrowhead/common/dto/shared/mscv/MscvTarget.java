package eu.arrowhead.common.dto.shared.mscv;


public class MscvTarget {

  private long id;
  private String name;
  private String os;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public String getOs() {
    return os;
  }

  public void setOs(String os) {
    this.os = os;
  }

}
